#include <stdio.h>

int main(void){
    float f = 0.3;
    double d = 10.67;
    printf("Float value: %f", f);
    printf("Double value: %f", d);
    return 0;
}

// Output
// Float value: 0.300000
// Double value: 10.670000

/* 

[NOTE]
Float and double are similar

- Double contains 15 digits
- Float contains 7 digits

*/